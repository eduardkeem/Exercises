
        function getMin(a, b) {
            if (a < b) {
                return a;
            } else {
                return b;
            }
        }

        console.log(getMin(2, 5));
        console.log(getMin(3, -1));
        console.log(getMin(1, 1));

        function min(a, b) {
        return a < b ? a : b;
        }

        console.log(min(2, 5));
        console.log(min(3, -1));
        console.log(min(1, 1));