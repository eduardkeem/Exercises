
        function isEven(n) {
            if (n == 0) {
                return "0 четный";
            } else if (n == 1) {
                return "1 нечетная";
            } else if (n % 2 == 0) {
                return "Четное число";
            } else {
                return "Нечетное число";
            }

        }
